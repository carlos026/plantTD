export function generateMapGrid(mapId) {
    const grid = [];
    for (let y = 0; y < MAP_H; y++) {
        const row = [];
        for (let x = 0; x < MAP_W; x++) {
            row.push(isRoad(mapId, x, y) ? 1 : 0);
        }
        grid.push(row);
    }
    return grid;
}

